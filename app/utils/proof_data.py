def get_proof_by_id(proof_id):
    """根据ID获取证明题"""
    for proof in PROOFS:
        if proof['id'] == proof_id:
            return proof
    return None


def get_proofs_by_chapter(chapter):
    """根据章节获取证明题"""
    if not chapter:
        return PROOFS
    return [p for p in PROOFS if p['chapter'] == chapter]


def get_proofs_by_difficulty(difficulty):
    """根据难度获取证明题"""
    if not difficulty:
        return PROOFS
    return [p for p in PROOFS if p['difficulty'] == difficulty]